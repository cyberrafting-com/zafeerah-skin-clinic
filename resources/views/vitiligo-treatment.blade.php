                            <h3 class="title">
                                Understanding Vitiligo

                                <span></span>
                            </h3>
                            <p class="text">Before exploring treatment options, let's first understand what vitiligo is and how it affects the skin. Vitiligo is a pigmentation disorder characterized by the loss of melanin, the pigment that gives skin its color. This loss of pigment results in white patches or depigmentation on the skin, which can appear anywhere on the body. While the exact cause of vitiligo is not fully understood, it is believed to involve a combination of genetic, autoimmune, and environmental factors.

                            </p>
