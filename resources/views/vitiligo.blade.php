                                <h5 class="service-title1 ">Symptoms of Vitiligo
                                </h5>

                                <p>The main symptom of vitiligo is the appearance of depigmented (white) patches on the skin. Other common symptoms include:
                                </p>                               
                                                          
                                <ul class="service-ul sep">
                                    <li>
                                    <strong>Loss of Color in Hair:</strong> Vitiligo can also cause the loss of pigment in hair, leading to white or grey hair in affected areas.
                                    </li>
                                    <li>
                                      <strong>Symmetrical Patterns: </strong>In many cases, vitiligo patches appear symmetrically on both sides of the body, such as on both hands or both elbows.
                                    </li>
                                    <li>
                                   <strong>Affected Mucous Membranes:</strong>  In rare cases, vitiligo can also affect the mucous membranes. 
                                    </li>                                   
                                 

                                </ul>   
                                
                                <h5 class="service-title1 sep">When to See a Doctor?

                                </h5>
                                <p>It's advisable to consult a dermatologist if you notice any signs of vitiligo, especially if:
                                </p>
                                                          
                                <ul class="service-ul sep">
                                    <li>You observe new depigmented patches on your skin.
                                  
                                    </li>
                                    <li>The existing patches are spreading or changing in size.
                                    
                                    </li>
                                    <li>You experience emotional distress or self-esteem issues due to vitiligo.
                                     </li>                              
                                  
                                 

                                </ul>   
                                <h5 class="service-title1 sep">Causes of Vitiligo

                                </h5>
                                <p>Possible causes and risk factors of vitiligo include:
                                </p>
                                                          
                                <ul class="service-ul sep">
                                    <li><strong>Autoimmune Response:</strong> In vitiligo, the immune system may mistakenly attack and destroy melanocytes, the cells responsible for producing pigment.
                                   
                                    </li>
                                    <li> <strong>Genetics:</strong> A family history of vitiligo or other autoimmune conditions can increase the chances of developing the condition.
                                   
                                    </li>
                                    <li> <strong>Environmental Triggers:</strong> Exposure to some environmental factors, like stress, sunburn, or chemicals, may trigger or exacerbate vitiligo in susceptible individuals.
                                     </li>                                
                                    
                                </ul> 
                                
                                <h5 class="service-title1 sep">Non-Surgical Interventions

                                </h5>
                                <p>Vitiligo clinics offer various non-surgical treatment options to manage the condition and pigment in the affected areas. These may include:
                                </p>
                                                          
                                <ul class="service-ul sep">
                                    <li><strong>Topical Steroids:</strong> Corticosteroid creams or ointments can help reduce inflammation and encourage repigmentation.
                                   
                                    </li>
                                    <li> <strong>Phototherapy:</strong> Light-based therapies, such as narrowband ultraviolet B (NB-UVB) therapy or excimer laser treatments, can stimulate melanocyte activity and promote repigmentation.
                                   
                                    </li>
                                    <li> <strong>Topical Immunomodulators: </strong> These medications modulate the immune response and may be used in combination with other treatments for better results.
                                     </li>                                
                                    
                                </ul> 

                                <h5 class="service-title1 sep">Surgical Interventions

                                </h5>
                                <p>Surgical interventions include:
                                </p>
                                                          
                                <ul class="service-ul sep">
                                    <li><strong>Skin Grafting:</strong> Healthy skin from unaffected areas of the body is transplanted to vitiligo-affected areas.
                                   
                                    </li>
                                    <li> <strong>Melanocyte Transplantation:</strong> Melanocytes are extracted from healthy skin and transplanted into depigmented areas to encourage pigment production.
                                   
                                    </li>
                                                                
                                    
                                </ul> 

                                <h5 class="service-title1 sep">Risk Factors and Complications

                                </h5>
                                <p>Risk factors for vitiligo include a family history of the condition, autoimmune diseases, and certain environmental triggers. Complications associated with vitiligo may include:
                                </p>
                                                          
                                <ul class="service-ul sep">
                                    <li>Emotional distress, anxiety, or depression due to changes in appearance.
                                   
                                    </li>
                                    <li>Increased sensitivity to sunlight and a higher risk of sunburn in depigmented areas.
                                   
                                    </li>
                                    <li>Social stigma or discrimination, especially in cultures where skin colour plays a significant role.</li>
                                                                
                                    
                                </ul> 
                                <h5 class="service-title1 sep">Prevention of Vitiligo


                                </h5>                             
                                                          
                                <ul class="service-ul sep">
                                    <li>Use sunscreen with a high SPF to protect depigmented areas.
                                   
                                    </li>
                                    <li> Practice stress-reducing techniques like meditation, yoga, or counselling to minimize triggers that may exacerbate vitiligo.
                                   
                                    </li>
                                    <li> Prompt diagnosis and treatment can help control vitiligo and prevent further depigmentation.</li>
                                    <li>Be cautious with chemical exposure, including hair dyes and skin care products that may irritate sensitive skin.</li>
                                                                
                                    
                                </ul> 

